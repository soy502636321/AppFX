package fx.hq.ui.entity;

public class BaseOrganEntity {
	private String organPid;
	private String organSms;
	private String remark;

	public String getOrganPid() {
		return organPid;
	}

	public void setOrganPid(String organPid) {
		this.organPid = organPid;
	}

	public String getOrganSms() {
		return organSms;
	}

	public void setOrganSms(String organSms) {
		this.organSms = organSms;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	
}
